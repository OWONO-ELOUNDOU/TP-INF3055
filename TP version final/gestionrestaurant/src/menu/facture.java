    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            String art = txtarticle.getText();
            int  qtite = (Integer)txtspinner.getValue();
            double prix = Double.parseDouble(txtprix.getText());
            double prixt= prix*qtite;
            
            m.addRow(new Object[]{
                art,
                qtite,
                prix,
                prixt
            });
             prixtotal += prixt;
             txtlabelprixt.setText("prix total de la facture : "+prixtotal+" francs");
        }catch(Exception e){
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
       //retourne l'indice d'une ligne du tableau ou -1 si celle ci n'est pas trouver
        int indicet = jTable1.getSelectedRow();
        
        if(indicet == -1){
            int indicejtxt = Integer.parseInt(jTextField3.getText());
            double prixartsup = (double)jTable1.getValueAt(indicejtxt, 3);
            prixtotal -= prixartsup;
            txtlabelprixt.setText("prix total de la facture : "+prixtotal+" francs");
            m.removeRow(indicejtxt);
        }else{
            double prixartsup = (double)jTable1.getValueAt(indicet, 3);
            prixtotal -= prixartsup;
            txtlabelprixt.setText("prix total de la facture : "+prixtotal+" francs");
            m.removeRow(indicet);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
      try{
          JFileChooser f = new JFileChooser();
          f.showDialog(this,"charger signature");
      File fichier= f.getSelectedFile();
      BufferedImage img = ImageIO.read(fichier);
      img.getScaledInstance(labelsignature.getWidth(),
                            labelsignature.getHeight(),Image.SCALE_SMOOTH );
      //construction de l'image a partir de l√®image tampon
      labelsignature.setIcon(new ImageIcon(img));
      
      //construction de l'image a partir du fichier
      //labelsignature.setIcon(new ImageIcon(fichier.getAbsolutePath()));
      }catch(Exception e){
          JOptionPane.showMessageDialog(null," erreur de chargement de signature");
      }
      
    }//GEN-LAST:event_jButton2ActionPerformed

